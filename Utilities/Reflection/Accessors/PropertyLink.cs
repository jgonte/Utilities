
namespace Utilities
{
    /// <summary>
    /// Links a source property with a target one
    /// </summary>
    public class PropertyLink
    {
        /// <summary>
        /// The name of the source property
        /// </summary>
        public string Source { get; set; }

        /// <summary>
        /// The name of the target property
        /// </summary>
        public string Target { get; set; }
    }
}
